// Function to extract brand from model name
export const extractBrandFromModel = (modelName: string): string => {
  const parts = modelName.split(' ');
  if (parts.length >= 2) {
    // Handle special cases like "ABT Audi", "Mercedes-Benz", etc.
    if (parts[0] === 'ABT') {
      return parts[1]; // Return the actual brand after ABT
    }
    if (parts[1] === 'Romeo') return 'Alfa Romeo';
    if (parts[1] === 'Romeo') return 'Alfa Romeo';
    if (parts[1] === 'Martin') return 'Aston Martin';
    if (parts[1] === 'Romero') return 'Alfa Romeo';
    if (parts[1] === 'Romero') return 'Alfa Romeo';
    if (parts[1] === 'Martin') return 'Aston Martin';
    if (parts[1] === 'Rover') return 'Land Rover';
    if (parts[1] === 'Benz') return 'Mercedes-Benz';
    if (parts[1] === 'Rover') return 'Land Rover';
    if (parts[1] === 'Benz') return 'Mercedes-Benz';
    if (parts[1] === 'Rover') return 'Land Rover';
    if (parts[1] === 'Benz') return 'Mercedes-Benz';
    return parts[1];
  }
  return 'Other';
};